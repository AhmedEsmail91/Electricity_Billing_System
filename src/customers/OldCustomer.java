    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        
        if(isnumric(edit_meterCode.getText())){
            if(!edit_meterCode.getText().isEmpty())
            {
                int  meterCode= Integer.parseInt(edit_meterCode.getText());
                int total_bill=r.getBill(meterCode);
                int unpaid=r.NumberUnpaidMonths(meterCode);
                if(total_bill==-999){
                    edit_bill.setText("Your bills have been paid\nThere's no bills for you.");
                }
                else{
                        if(unpaid>=3)
                        edit_bill.setText("Your recent bill: "+total_bill+" EGP\nyou have "+unpaid+" upaid unpaid bills\npaid Bills: "+r.NumberPaidMonths(meterCode)+"\nplease contact us on 256486 to \npay the bills or\nyour subscription will be stopped");
                        else
                            edit_bill.setText("recent Bill: "+total_bill+"\nyou have "+unpaid+" upaid unpaid bills\npaid Bills: "+r.NumberPaidMonths(meterCode)+"");
                        }
            }
        }
        else {
              
            JOptionPane.showMessageDialog(null,"Enter Data(MeterCode)",
                null, JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_btn_submitActionPerformed

