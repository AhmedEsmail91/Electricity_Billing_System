    private void btn_payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_payActionPerformed
        // TODO add your handling code here:

        //Check if the form filled
        if(!edit_meterCode.getText().isEmpty() )
        {
            if(isnumric(edit_meterCode.getText())){
                int  meterCode= Integer.parseInt(edit_meterCode.getText());
                r.update("bill", "paid", "1", "bill_date='"+r.getBill_date(meterCode)+"' and meter_code="+meterCode);
            JOptionPane.showMessageDialog(null, "Thanks for dealing with us");
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter the meter code in digits",
                null, JOptionPane.ERROR_MESSAGE);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please enter the meter code",
                null, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_payActionPerformed

